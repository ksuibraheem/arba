// قراءة BOQ
const wb = XLSX.readFile(BOQ_PATH);
const ws = wb.Sheets[wb.SheetNames[0]];
const raw = XLSX.utils.sheet_to_json(ws, {header:1, defval:''});

let totalV2 = 0, totalV4 = 0;
const analysis = [];

for(let i = 4; i < raw.length; i++) {
  const num = parseInt(raw[i][0]);
  if(isNaN(num) || !v2[num]) continue;
  const qty = parseFloat(raw[i][3]) || 0;
  if(!qty) continue;
  
  const oldTotal = v2[num].r * qty;
  const newTotal = v4[num].r * qty;
  const diff = oldTotal - newTotal;
  const pct = ((diff / oldTotal) * 100).toFixed(0);
  
  totalV2 += oldTotal;
  totalV4 += newTotal;
  
  let severity = '✅';
  if(pct > 80) severity = '🔴 خطأ فادح';
  else if(pct > 50) severity = '🟠 مبالغة كبيرة';
  else if(pct > 30) severity = '🟡 تضخم';
  else if(pct > 15) severity = '🔵 تضخم طفيف';
  
  analysis.push({
    no: num, qty, note: v2[num].note, problem: v2[num].problem,
    oldRate: v2[num].r, newRate: v4[num].r,
    oldTotal, newTotal, diff, pct: parseInt(pct), severity
  });
}

// ترتيب حسب أكبر فرق
analysis.sort((a,b) => b.diff - a.diff);

